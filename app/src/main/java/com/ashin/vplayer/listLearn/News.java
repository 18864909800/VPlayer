package com.ashin.vplayer.listLearn;

public class News {
    //新闻标题
    public String title;
    //新闻内容
    public String content;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
